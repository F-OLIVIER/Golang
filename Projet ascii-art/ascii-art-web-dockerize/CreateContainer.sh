#!/bin/bash

# Stoppe le container nommé
echo "****************************************************************************************************"
echo "**************************************** Arrêt du container. ***************************************"
echo "****************************************************************************************************"
docker stop ascii_art_dockerize
echo "****************************************** Container arrêté.****************************************"
echo
# Supprime le container nommé
echo "****************************************************************************************************"
echo "************************************* Suppression du container. ************************************"
echo "****************************************************************************************************"
docker rm ascii_art_dockerize
echo "**************************************** Container supprimé. ***************************************"
echo
# Vidage du cache.
echo "****************************************************************************************************"
echo "***************************************** Vidage du cache. *****************************************"
echo "****************************************************************************************************"
docker image prune
echo "******************************************** Cache vidé. *******************************************"
echo
# Création du container nommé
echo "****************************************************************************************************"
echo "************************** Création du container nommé de l'image nommée. **************************"
docker container create --name ascii_art_dockerize ascii_art_dockerize
echo "****************************************** Container créé. *****************************************"
echo